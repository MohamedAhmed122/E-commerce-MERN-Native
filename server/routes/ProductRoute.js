import express from 'express'
import Product from '../models/productsModel.js'
import Category from '../models/categoryModel.js'
import multer from 'multer'

import {
    getProducts, 
    getProductById, 
    updateProduct,
    deleteProduct,
    getProductCount,
    getProductFeatured,
    filterProductByCategory
 } from '../controllers/ProductController.js'

 import {protect, admin } from '../middleware/authMiddleware.js'

const router = express.Router()



router.route('/').get(filterProductByCategory)
router.route('/').get(getProducts)
router.route('/:id').get(getProductById)
router.route('/:id').put(protect, admin, updateProduct)
router.route('/:id').delete(protect, admin, deleteProduct)
router.route('/get/count').get(getProductCount) 
router.route('/get/featured/:count').get(getProductFeatured) 

const FILE_TYPE ={
  'image/png' : 'png',
  'image/jpeg' : 'jpeg',
  'image/jpg' : 'jpg'
}

const storage = multer.diskStorage({
    destination: function (req, file, cb) {
      const isValidate = FILE_TYPE[file.mimetype]
      let uploadError = new Error('Invalid Image Type')
      if(isValidate){
        uploadError =null
      }
      cb(uploadError, 'public/uploads')
    },
    filename: function (req, file, cb) {
        
      const fileName = file.originalname.split(' ').join('-');
      const extension = FILE_TYPE[file.mimetype]
      cb(null, `${fileName}-${Date.now()}.${extension}`)
    }
  })
  
const uploadOptions = multer({ storage: storage })

router.post(`/`, uploadOptions.single('image'), async (req, res) =>{
    const category = await Category.findById(req.body.category);
    if(!category) return res.status(400).send('Invalid Category')

    const file = req.file;
    if(!file) return res.status(400).send('Invalid Image')

    const fileName = req.file.filename
    const basePath = `${req.protocol}://${req.get('host')}/public/uploads/`;
    let product = new Product({
        name: req.body.name,
        description: req.body.description,
        richDescription: req.body.richDescription,
        image: `${basePath}${fileName}`,// "http://localhost:3000/public/upload/image-2323232"
        brand: req.body.brand,
        price: req.body.price,
        category: req.body.category,
        countInStock: req.body.countInStock,
        rating: req.body.rating,
        numReviews: req.body.numReviews,
        isFeatured: req.body.isFeatured,
    })

    product = await product.save();

    if(!product) 
    return res.status(500).send('The product cannot be created')

    res.send(product);
})



export default router